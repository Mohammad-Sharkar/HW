package oop; //Encapsulation

public class Travelers extends Expidea{
	
	private String TraName;
	private String TraAddress;
	private String TraPassportNo;
	private int TraAge;
	
	public String getTraName() {
		return TraName;
	}
	public void setTraName(String traName) {
		TraName = traName;
	}
	public String getTraAddress() {
		return TraAddress;
	}
	public void setTraAddress(String traAddress) {
		TraAddress = traAddress;
	}
	public String getTraPassportNo() {
		return TraPassportNo;
	}
	public void setTraPassportNo(String traPassportNo) {
		TraPassportNo = traPassportNo;
	}
	public int getTraAge() {
		return TraAge;
	}
	public void setTraAge(int traAge) {
		TraAge = traAge;
	}
	
	
	
	

}
